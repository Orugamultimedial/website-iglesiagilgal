                LA PALABRA COMO FUENTE DE LA VERDADERA SABIDURÍA<br>
                El salmista describe la Palabra como “lámpara a mis pies, y luz para mi camino” (Sal. 119:105). La idea de que la Palabra es como una “lámpara” o “luz” apunta a su capacidad de indicar el camino a seguir, de evitar los errores y tropiezos típicos del que anda en oscuridad. 
