                TODO PARA LA GLORIA DE DIOS<br>
                La propia Palabra indica que “todo ha sido creado por medio de El y para El.” (Col. 1:16 ). En otras palabras, Dios es no sólo el origen de nuestra existencia (“por El…”) sino el propósito de la misma (“…para El”).
