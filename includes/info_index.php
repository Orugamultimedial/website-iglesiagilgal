                La Palabra contiene diversas exhortaciones para que nuestra vida imite y refleja la de Cristo. Jesús mismo nos llama en Mat. 11:29 “…aprended de mí, que soy manso y humilde de corazón…”. Por medio de èl y solo en èl encontramos salvaciòn.    
